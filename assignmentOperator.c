#include<stdio.h>
int main()
{
    printf("All Assignment Operator - =,+=,-=,*=,/=,%=,!=,<<=,>>=,&=,^= \n");
    int a = 10;
    printf("%d \n",a=10);
    printf("%d  \n",a+=10);
    printf("%d  \n",a-=10);
    printf("%d  \n",a*=10);
    printf("%d  \n",a/=10);
    printf("%d  \n",a%=10);
    printf("%d  \n",a!=10);
    printf("%d  \n",a<<=10);
    printf("%d  \n",a>>=10);
    printf("%d  \n",a&=10);
    printf("%d  \n",a^=10);
}